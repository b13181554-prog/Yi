// Get current language from document or default to 'ar'
function getCurrentLanguage() {
  return document.documentElement.getAttribute('lang') || 'ar';
}

// Translation function - gets translation for a key in current language
function t(key) {
  const lang = getCurrentLanguage();
  const langTranslations = translations[lang] || translations.ar;
  return langTranslations[key] || translations.ar[key] || key;
}

// Apply translations to all elements with data-i18n attribute
function applyTranslations() {
  const lang = getCurrentLanguage();
  console.log('ğŸŒ Applying translations for language:', lang);
  
  // Find all elements with data-i18n attribute
  const elements = document.querySelectorAll('[data-i18n]');
  
  elements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    const translation = t(key);
    
    // Update element content based on type
    if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
      // For input elements, update placeholder
      if (element.hasAttribute('placeholder')) {
        element.setAttribute('placeholder', translation);
      }
    } else if (element.tagName === 'OPTION') {
      // For option elements, update text content
      element.textContent = translation;
    } else {
      // For other elements, update text content
      element.textContent = translation;
    }
  });
  
  console.log(`âœ… Applied ${elements.length} translations`);
}

// Export functions for use in other scripts
if (typeof window !== 'undefined') {
  window.t = t;
  window.applyTranslations = applyTranslations;
  window.translations = translations;
}
